    }

    /**
     * Handle the User "deleted" event.
     */
    public function deleted(User $user): void
    {
        $tools = Tool::where('user_id', $user->id)->get();
        
        foreach ($tools as $tool) {
            if ($tool->image) {
                Storage::disk('public')->delete($tool->image);
            }

            $tool->delete();
        }
    }

